import java.util.Scanner;
class Type_casting
{
  public static void main(String[] args)
  {
     char c='a';
     int n;
     n=c;
     System.out.println("num="+n);
     float b=2.54f;
     int d;
     d=(int)b+1;
     System.out.println("d="+d);
     System.out.println("b="+b);
  }
}
